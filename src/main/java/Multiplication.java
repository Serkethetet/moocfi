public class Multiplication {
    public static void main(String[] args) {
        int factor1 = 2;
        int factor2 = 8;
        int product = factor1 * factor2;
        System.out.println(factor1 + " * " + factor2 + " = " + product);
        factor1 = 277;
        factor2 = 111;
        product = factor1 * factor2;
        System.out.println(factor1 + " * " + factor2 + " = " + product);
        factor1 = 999999999;
        factor2 = 999999999;
        product = factor1 * factor2;
        System.out.println(factor1 + " * " + factor2 + " = " + product);
    }
}
