public class Addition {
    public static void main(String[] args) {
        int summand1 = 5;
        int summand2 = 4;
        int sum = summand1 + summand2;
        System.out.println(summand1 + " + " + summand2 + " = " + sum);
        summand1 = 73457;
        summand2 = 12888;
        sum = summand1 + summand2;
        System.out.println(summand1 + " + " + summand2 + " = " + sum);
    }
}
